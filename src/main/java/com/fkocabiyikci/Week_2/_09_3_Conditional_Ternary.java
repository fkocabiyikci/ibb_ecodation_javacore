package com.fkocabiyikci.Week_2;


public class _09_3_Conditional_Ternary {

    // Ternary Operatörü (? :), if-else yapısının kısa hali olarak kullanılır.
    public static void main(String[] args) {
        int number = 5;
        String result = (number > 0) ? "Pozitif sayıdır" : "Negatif Sayıdır";
        System.out.println(result);
    }
}